const mongoose = require("mongoose");

// Mongoose Model
var userSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});


// Export Mongoose "User" model
module.exports = mongoose.model("User", userSchema);