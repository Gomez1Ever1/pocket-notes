const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Mongoose Model
const userSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

const User = mongoose.model("User", userSchema);

// Export Mongoose "User" model
module.exports = User;
